public class conditionals
{
    public static void main(String[] args)
    {
        final int EXAMPLE = 32;

        if (EXAMPLE == 32) {
            System.out.printf("Example is 32");
        } else if (EXAMPLE == 64) {
            System.out.printf("Example is 64");
        } else {
            System.out.printf("Example is neither 32 or 64");
        }
    }
}
